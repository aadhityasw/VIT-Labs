DATA SEGMENT
    STR1 DB "hello$"
    STR2 DB "hello$"
    NEWLINE DB 10,13,"$"
    MSG1 DB "BOTH STRING ARE SAME$"
    MSG2 DB "BOTH STRING ARE DIFFERENT$"
DATA ENDS

CODE SEGMENT

    ASSUME DS:DATA,CS:CODE
START:  
    MOV AX,DATA
    MOV DS,AX

    LEA SI,STR1
    LEA DI,STR2

STRING_COMPARISION :
    MOV BX,00

    MOV BL,STR1+1
    MOV BH,STR2+1

    CMP BL,BH
    JNE L1

    ADD SI,2
    ADD DI,2

  L2:MOV BL,BYTE PTR[SI]
    CMP BYTE PTR[DI],BL
    JNE L1
    INC SI
    INC DI
    CMP BYTE PTR[DI],"$"
    JNE L2

    MOV AH,09H
    LEA DX,MSG1
    INT 21H

    JMP L5

  L1:MOV AH,09H
    LEA DX,MSG2
    INT 21H



 L5:
    MOV AH,09H
    LEA DX,NEWLINE
    INT 21H

    MOV AH,4CH
    INT 21H

hlt
CODE ENDS
END START

