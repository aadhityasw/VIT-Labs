ASSUME CS:CODE, DS:DATA
DATA SEGMENT
	NUM DB 05H
	RE DB 02H
	NUM1 DB 00H
	DEN DB 00H
DATA ENDS
CODE SEGMENT

FACT PROC NEAR
	CMP CL,00H
	JE RETURN
	MUL CL
	DEC CL
	CALL FACT
	RETURN: RET
FACT ENDP
START:
	MOV AX,DATA
	MOV DS,AX
	MOV AX,0000H
	MOV AL,NUM
	MOV DL,AL
	SUB DL,01H
	MOV CL,DL
	CALL FACT
	MOV DEN,AL
	MOV AX,0000H
	MOV AL,NUM
	MOV BL,RE
	SUB AL,BL
	MOV DL,AL
	SUB DL,01H
	MOV CL,DL
	CALL FACT
	MUL DEN
	MOV BL,AL
	MOV AX, 00H
	MOV AL,NUM1
	DIV BL
	HLT
CODE ENDS
END START
